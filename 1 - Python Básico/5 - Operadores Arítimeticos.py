"""
+  | Serve para a soma de um valor
-  | Serve para a subtração de um valor
*  | Serve para a multiplicação de um valor
// | Serve para o arredondamento de uma divisão
** | Serve  para a exponênciação de um valor
%  | Serve para retornar o resto de uma divisão
() | Serve para alterar as precedências de uma conta

"""

print(10 + 10 )  # Soma
print(10 - 5  )  # Subtração
print(10 * 10 )  # Multiplicação
print(10 / 10 )  # Divisão
print(10 % 10 )  # Resto
print(10 // 3 )  # Divisão arredondada
print(10 ** 2 )  # Potênciação


# É possível multiplicar uma string
print("Bla" * 10 )

#  Precedência
print((5 + 2 ) * 10 )

# Concatenação
print('10' + 10 )  # Isso irá trazer um erro
print('10' + '10') # Duas strings se somadas iram concatenar(juntar)

